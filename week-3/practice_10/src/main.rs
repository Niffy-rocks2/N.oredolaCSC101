fn main() {
    // Addition
    let sum = 10 + 20;
    println!("The sum of 10 and 20 is: {}", sum);

    // Subtraction
    let difference = 95.5 - 4.3;
    println!("The difference between 95.5 and 4.3 is: {}", difference);

    // Multiplication
    let product = 4 * 30;
    println!("The product of 4 and 30 is: {}", product);

    // Division
    let quotient = 56.7 / 3.2;
    println!("The quotient of 56.7 and 3.2 is: {}", quotient);

    // Remainder
    let remainder = 43 % 5;
    println!("The remainder of 43 divided by 5 is: {}", remainder);
}

